                        <div class="col-12 col-md-10 offset-md-1">
                            <p class="article">A Associação Nacional de Segurança e Transporte de Valores (ANSEGTV) é lançada para fomentar a livre concorrência e a cultura de boas práticas na segurança privada. A entidade desenvolve grupos de estudo, eventos, missões, pesquisas e difusão. Todos convergindo para elevar a qualidade do serviço e ampliar o atendimento no setor de segurança privada e transporte de valores. A sede em Brasília foi inaugurada em 27 de julho de 2019.</p>

                            <p class="article">A entidade nasce com 12 empresas associadas que estão presentes em 24 estados e no Distrito Federal. “Temos um mercado muito importante para o funcionamento da economia e da sociedade, mas que precisa se modernizar com novos investimentos e com uma saudável desconcentração”, diz o diretor presidente da ANSEGTV, Gabriel Montenegro Damasceno. “Novos investimentos significam uma nova mentalidade, principalmente para dotar o transporte de valores, numerários e a segurança privada como um todo mais segura”, afirma. O mercado de segurança privada no Brasil emprega mais de 500 mil trabalhadores formais e tem faturamento anual de R$ 33 bilhões.</p>

                            <p class="article">Para incentivar uma nova mentalidade no transporte de valores, a ANSEGTV atuará não apenas como uma representante institucional das empresas perante as autoridades. “Queremos fazer parcerias para contribuir com a segurança pública e, complementarmente, funcionar como um observatório para mensurar os efeitos das boas práticas no nosso mercado”, afirma.</p>

                            <p class="article">A livre concorrência é uma condição primordial, segundo Damasceno, para que a segurança privada seja modernizada no Brasil. Um dos focos da entidade é o estudo da reestruturação do marco regulatório do segmento, tratada em projeto que atualmente tramita no Senado. Se aprovado, o Substitutivo da Câmara dos Deputados nº6 de 2016 (SCD 6/2016) criará o Estatuto da Segurança Privada.  “Nosso objetivo é que esta lei modernize a legislação do segmento, crie mais empregos e garanta maior atração de investimentos, respeitando o livre mercado e a livre iniciativa”, diz o presidente da ANSEGTV”.</p>

