                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">A Reforma Tributária segue no centro do debate econômico. O presidente da Câmara, Rodrigo Maia (DEM-RJ), e do Senado, Davi Alcolumbre (DEM-AP), já declararam que pretendem aprovar a medida até o fim deste ano.</p>
                            <p class="article">Atualmente, tramitam no Congresso duas PECs (110 e 45/2019) sobre o tema, além do Projeto de Lei 3.887/2020, entregue pelo governo federal em 21 de julho.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/08/congresso_ansegtv_agosto.jpg" width="480" height="auto" alt="Congresso Nacional" class=" img-fluid">	
                                <figcaption>Foto: Jefferson Rudy/Agência Senado</figcaption>
                            </figure>
                            <p class="article">Resumidamente, a PEC 110 consiste na extinção de nove tributos e a criação de um IBS dual (IBS Federal e IBS Subnacional), sem grandes incentivos fiscais, isenções, redução de base de cálculo ou crédito presumido. A PEC 45 propõe a extinção de cinco tributos e a criação de um IVA (imposto sobre valor agregado), que seria denominado IBS (imposto sobre bens e serviços). Já a proposta do governo federal é de uma reforma tributária fatiada. Na primeira fase, seria criada a CBS (Contribuição sobre Bens e Serviços), com as mesmas características do IBS da proposta da PEC 45, porém com a aplicação de uma alíquota de 12% e extinção de apenas de dois tributos (PIS e Cofins).</p>
                            <p class="article">Os projetos são positivos ao extinguirem e unificarem impostos, além de trazerem maior segurança jurídica e transparência. Por outro lado, ainda sobrecarregam sobremaneira o setor de serviços (o que mais emprega no país), com incidência ampla de tributação sobre o consumo e poucas opções de créditos a compensar.</p>
                            <p class="article">A força motriz do setor sempre foi a mão de obra. Esta deveria ser a base da discussão, mas é pouco mencionada ou ignorada. Sem previsão de contrapartidas financeiras para a área, a desoneração da folha de pagamentos torna-se imprescindível.</p>
                            <p class="article">O processo de retomada econômica é lento e deve se estender pelos próximos anos.</p>
                            <p class="article">Em um momento de crise como agora, compreende-se a necessidade do governo em ampliar sua capacidade de obter recursos. É preciso, no entanto, encontrar um meio-termo para que determinados segmentos da economia não sejam mais prejudicados que outros.</p>
