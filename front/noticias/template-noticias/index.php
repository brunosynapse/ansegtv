                                <p class="desc">Muito se discute atualmente a respeito do crescimento de pagamentos virtuais em detrimento da utilização do dinheiro em espécie.</p>
                                <p class="desc">Impossível ignorar os benefícios trazidos pela tecnologia, mas o anúncio pelo Banco Central da nova cédula de R$ 200 mostra que o dinheiro vivo segue com vital importância.</p>
                                <p class="desc">De março até julho, segundo a autarquia federal, a quantidade de cédulas em circulação subiu de R$ 216 bilhões para R$ 277 bilhões, um aumento de 29% e recorde da série histórica iniciada em dezembro de 2001.</p>
                                <h3>Cédula 200 reais</h3>
                                <p class="desc">Isso só foi possível graças aos esforços de governo e transportadoras de valores. A pandemia trouxe desafios, mas as empresas souberam se adaptar e mantiveram o serviço de qualidade.</p>
                                <p class="desc">Com a crise econômica, a tendência é que a demanda pelo numerário se mantenha e até cresça. Em momentos como agora, por exemplo, o entesouramento aumenta. Ou seja, receosa sobre o futuro, a população saca e guarda o seu dinheiro.</p>
                                <p class="desc">Ainda é preciso aguardar maiores detalhes pelo Planalto sobre a introdução da nova cédula no mercado, mas a ANSEGTV e suas associadas avaliam como positiva a medida e estarão preparadas para seguir atendendo a todos.</p>
