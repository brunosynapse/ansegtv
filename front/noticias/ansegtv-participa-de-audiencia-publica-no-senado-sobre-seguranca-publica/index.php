                            <h1>ANSEGTV participa de audiência pública no Senado sobre Estatuto da Segurança Privada</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>18/09/2019 - 16h12</span> | <span class="mx-2 category"><i class="fa fa-tag"></i>Transporte de Valores</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1">
                            <p class="article">O presidente da Associação Nacional de Segurança e Transporte de Valores (ANSEGTV), Gabriel Damasceno, participou de audiência pública da Comissão de Transparência, Governança, Fiscalização e Controle e Defesa do Consumidor, que ocorreu no Senado Federal para dar contribuições ao debate em torno do Estatuto da Segurança Privada e da Segurança das Instituições Financeiras. A proposição tramita há quase dez anos no Congresso Nacional e iniciou-se voltada a contemplar direitos da categoria dos trabalhadores da segurança privada por meio do Projeto de Lei do Senado - PLS nº 135/2010. Na Câmara dos Deputados, foram incluídos vários dispositivos, entre eles, a proibição da participação de instituições financeiras no capital de empresas de transporte de valores. A restrição viola a livre concorrência e a livre iniciativa garantidas pela Constituição Federal de 1988 e pode impactar empresas do ramo.</p>
                            <p class="article">A proposição voltou ao Senado Federal com o nome Substitutivo da Câmara dos Deputados nº6 de 2016. O SCD 6/2016 hoje trata, além da regulamentação da atividade de segurança privada, do plano de segurança em dependências de instituições financeiras, da fiscalização da Polícia Federal para estes serviços, do funcionamento das escolas de formação de vigilante privado, entre outros pontos. O projeto é considerado pela ANSEGTV extremamente positivo para o setor de Segurança Privada, com exceção dos parágrafos 2º ao 5º do Art. 20, incluídos no texto sem ampla discussão ou divulgação prévia.</p>
                            <img src="../../img/uploads/2019/09/senado-comissao-transparencia_ansegtv.jpeg" width="490" height="auto" alt="Comissão da Transparência do SENADO" class="pull-left m-2 img-fluid">
                            <p class="article">Em sua exposição na audiência pública no dia 8 de agosto de 2019, Gabriel Damasceno afirmou que o Art. 20, aprovado na Câmara, além de fugir do objeto do estatuto, reduz ainda mais a concorrência do setor, já dominado por grandes players multinacionais, entre eles um espanhol e um norte-americano. O mercado está concentrado em apenas três empresas, que detêm 80% do setor. "Empresas que hoje estão no Brasil e continuarão têm participação de bancos internacionais e querem proibir, por lei, que bancos brasileiros tenham participação nesse setor, inclusive, bancos públicos", resumiu o dirigente da ANSEGTV. O relator do projeto de lei, senador Randolfe Rodrigues (REDE -AP), também questionou a pertinência do dispositivo. “Não deveria existir restrição à participação de qualquer empresa, seja com capital estrangeiro, seja com capital nacional. Quando falamos de livre mercado e livre iniciativa, falamos de condições isonômicas e igualitárias.” </p>
                            <h3 class="subtitle">Seguro</h3>
                            <p class="article">A questão dos esforços para mitigar fragilidades dos serviços de transporte de valores também foi discutida na audiência pública. Nos últimos quatro anos, todos os ataques as Bases de Transporte de Valores ocorreram nas três maiores empresas que dominam o mercado. Sobre o dado preocupante, Gabriel Damasceno afirmou que as companhias devem investir mais em segurança, tecnologia e prevenção — e não apenas em aquisições de outras empresas do mercado. “Apesar do Brasil estar passando por um problema sério de segurança pública, as empresas têm que fazer a sua parte. Não podem colocar o ônus somente nos órgãos públicos". Os ataques as bases e a perda dos recursos resultam no aumento do seguro por parte das resseguradoras para todo o mercado, o que prejudica principalmente as pequenas e médias empresas, não atingidas necessariamente pelos roubos. Isso em um contexto em que as companhias já sofrem com a renovação da apólice do seguro, com reajustes de 100% a 300% ao ano.</p>

                            <p class="article">O diretor da Federação Brasileira de Bancos (FEBRABAN), Pedro Oscar Viotto, disse que a proibição das instituições financeiras nas empresas de transporte de valores traria efeitos negativos para o consumidor. "Tais restrições, se levadas a cabo, impactariam não apenas o setor bancário, mas todas as áreas que consomem esses serviços, como supermercados, hotéis, shoppings e o próprio poder público". </p>

                            <p class="article">Damasceno encerrou sua fala na audiência pública defendendo a aprovação célere do Estatuto da Segurança Privada, com a exclusão total dos parágrafos 2º ao 5º do Art. 20 por não estarem em consonância com os objetivos de uma legislação moderna, equilibrada e que forneça segurança jurídica ao setor. </p>
