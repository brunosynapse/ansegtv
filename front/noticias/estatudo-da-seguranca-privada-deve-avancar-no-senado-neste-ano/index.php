                            <h1>Estatuto da Segurança Privada deve avançar no Senado neste ano</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>21/01/2020 - 14h12</span> | <span class="mx-2 category"><i class="fa fa-tag"></i>Transporte de Valores</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1">
                            <p class="article">Depois de mais de dois anos parado no Senado, o Estatuto da Segurança Privada (PLS 135/2010) deve avançar em 2020. Os senadores precisam avaliar as mudanças sugeridas pela Câmara (SCD 6/2016) sobre o texto. A próxima etapa é na Comissão de Transparência, Governança, Fiscalização e Controle e Defesa do Consumidor (CTFC).</p>
                            <p class="article">A versão original do projeto apenas estabelece um piso salarial nacional para a categoria dos vigilantes, e foi aprovada pelo Senado em 2012. Na passagem pela Câmara, porém, o texto ganhou mais abrangência e passou a tratar de temas como atuação das empresas de segurança, regras para formação de profissionais e uso de armas e outros equipamentos controlados.<p>
                            <p class="article">O substitutivo da Câmara precisa passar por quatro comissões. Por enquanto ele tem apenas o aval da Comissão de Assuntos Sociais (CAS), que aprovou a maior parte das mudanças em 2017. Depois disso, o projeto, do então senador Marcelo Crivella, ficou paralisado até o fim de 2019, quando enfim recebeu o relatório para votação na CTFC.</p>
                            <h3 class="subtitle">Transporte de Valores</h3>
                            <p class="article">Uma polêmica que circunda o Estatuto é a atuação de bancos no mercado de transporte de valores. Um dos dispositivos do projeto proíbe que instituições financeiras tenham participação no capital de empresas de segurança privada. O assunto foi debatido pela CTFC numa audiência pública em novembro.</p>
                            <img src="../../img/uploads/2020/01/estatuto-seguranca-privada.png" width="490" height="auto" alt="Estatuto da Segurança Privada" class="pull-right m-2 img-fluid">
                            <p class="article">O senador Randolfe Rodrigues (Rede-AP), relator do substitutivo na CTFC, optou por remover a proibição. Ele argumenta que essa regra reduziria as possibilidades de concorrência e prejudicaria os consumidores. Para ele, o resultado prático da medida seria “promover um oligopólio”.</p>
                            <p class="article">“Não há no projeto qualquer justificativa plausível para essa medida de limitação da concorrência. Não há qualquer incompatibilidade econômica ou social no fato de as instituições financeiras serem proprietárias de empresas de segurança privada”, escreve o senador em seu relatório.</p>
                            <p class="article">Randolfe já havia tentado retirar esse dispositivo do substitutivo na sua passagem pela CAS, mas a iniciativa foi derrotada.</p>
                            <p class="article">Outra mudança promovida pelo senador foi sobre a regra de capital social mínimo que as empresas de segurança privada precisam comprovar antes de obterem autorização para operar. Na versão da Câmara, esse capital deveria ser comprovado em cada estado onde a empresa desejasse atuar. Randolfe também considerou que essa norma prejudicaria a ampla concorrência, e removeu a parte que se referia a cada estado, estabelecendo uma única verificação de capital mínimo.</p>
                            <p class="article">O SCD 6/2016 já pode ser incluído na pauta da CTFC. A comissão já sinalizou que a proposta será uma das primeiras tarefas do ano. Depois dela, as comissões de Direitos Humanos (CDH) e de Constituição e Justiça (CCJ) também deverão se manifestar.</p>
                            <p class="article">A palavra final sobre as intervenções da Câmara será do Plenário. Como o projeto se iniciou no Senado, não há necessidade de remetê-lo novamente para os deputados, e o texto final já poderá seguir para a sanção presidencial.</p>

                            <em>Fonte: Agência Senado</em>
