                            <h1>Importante, LGPD ainda gera dúvidas</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>24/09/2020 - 15h</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
