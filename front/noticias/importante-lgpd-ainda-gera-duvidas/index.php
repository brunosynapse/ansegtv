                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Após várias idas e vindas, a Lei Geral de Proteção de Dados Pessoais (LGPD) entrou em vigor no último dia 18 de setembro.</p>
                            <p class="article">Aprovada em 2018, ainda no governo Michel Temer, a legislação sofreu várias mudanças até passar a valer. O texto define direitos de indivíduos em relação as suas informações pessoais e regras para quem coleta e trata esses registros. O objetivo é proteger os direitos fundamentais de liberdade e privacidade e o livre desenvolvimento da personalidade dos cidadãos</p>

                            <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2020/09/lgpd-ansegtv.png" width="450" height="auto" alt="Logo Frente Parlamentar" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">Apesar de prever multas para empresas apenas a partir de agosto de 2021, a adaptação à nova lei exige atenção especial desde já.</p>
                            <p class="article">No setor de serviços, por ser um segmento onde a mão de obra tem papel fundamental, com atividades muitas vezes terceirizadas, as empresas contratantes deverão se atentar ainda mais as suas responsabilidades.</p>
                            <p class="article">Contratos devem delimitar as obrigações de cada uma das partes em relação à proteção de dados de funcionários, por exemplo, seja pela prestadora de serviços ou pela tomadora, para que nenhum dos lados saia prejudicado.</p>
                            <p class="article">Questões como essa ainda suscitam dúvidas e geram insegurança jurídica. A ANPD (Agência Nacional de Proteção de Dados), que deve fiscalizar o cumprimento da LGPD, ainda não foi instituída. Até o momento, apenas sua estruturação foi definida. Torna-se urgente sua criação de fato.</p>
                            <p class="article">A ANSEGTV entende que a privacidade é um direito fundamental, garantido em nossa Constituição de 1988. A LGPD chega para atender aos avanços tecnológicos de uma sociedade cada vez mais conectada. É preciso, no entanto, uma melhor regulamentação das normas.</p>
