                            <h1>Reforma Tributária no setor de serviços</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i> 07/12/2020 - 14h30</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Passadas as eleições municipais, as atenções se voltam à pauta legislativa. São vários os temas relevantes que devem ser discutidos ainda neste ano.</p>
                            <p class="article">A economia dá sinais de recuperação, mas com a pandemia ainda em alta, medidas que visem a retomada das atividades mostram-se ainda mais necessárias.</p>
                            <p class="article">Apesar de resultados que demonstram algum grau de reação de empresas, importante lembrar que a taxa de desemprego, um dos principais termômetros da economia de um país ainda é alta: 14,4% no trimestre encerrado em agosto, segundo o <a href="https://g1.globo.com/economia/noticia/2020/10/30/desemprego-no-brasil-sobe-para-144percent-em-agosto-diz-ibge.ghtml" terget="_blank">IBGE</a>.</p>
                            <p class="article">Em dezembro também está previsto a última parcela do auxílio-emergencial, o que deve impactar na queda do consumo e na situação das famílias, principalmente as de baixa renda.</p>
                            <p class="article">A Reforma Tributária tem papel importante neste processo de retomada. Em busca de um consenso, a discussão política em torno do assunto prossegue.</p>
                            <p class="article">Nos últimos dias, novas sugestões, como cobrança de IPVA sobre barcos e aeronaves, passaram a ser defendidas nos corredores do Congresso.</p>
                            <p class="article">Atualmente, a Constituição permite apenas a cobrança do imposto sobre veículos terrestres pelos estados. Com a mudança, a nova alíquota seria definida por cada unidade da Federação.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/12/ministerio-economia-fachada.jpg" width="450" height="auto" alt="Ministério da Economia - Marcello Casal Jr./Agência Brasil." class=" img-fluid">	
                                <figcaption>Marcello Casal Jr./Agência Brasil</figcaption>
                            </figure>
                            <p class="article">Outra proposta polêmica apresentada pela oposição é o fim da isenção ou dos incentivos em relação ao Imposto de Renda sobre lucros e dividendos e sobre juros de capital próprio.</p>
                            <p class="article">A medida vai contra o movimento mundial que é a simplificação de tributos. O Brasil é um dos países que mais taxam o lucro de empresas (34%). Para se ter uma ideia, nos <a href="http://repositorio.ipea.gov.br/bitstream/11058/8354/1/TD_2380.pdf" target="_blank">países nórdicos</a>, essa tributação gira em torno de 20%.</p>
                            <p class="article">Recentemente, o presidente Jair Bolsonaro abandonou a ideia de uma nova CPMF. A intenção do governo era pegar o valor angariado com o tributo a ser criado e promover a desoneração da folha de pagamentos.</p>
                            <p class="article">Um alento poderá vir com julgamento, pelo Superior Tribunal de Justiça (STJ), a respeito da limitação da base de cálculo das contribuições para terceiros (Incra, Sebrae, salário educação e o Sesi, Senai, Sesc, Senac e Senat) a 20 salários mínimos, que poderá ter efeito repetitivo. Em média, o peso dessas contribuições sobre a folha de pagamentos corresponde a 5,8% ao mês</p>
                            <p class="article">Com grande peso no PIB e um dos maiores responsáveis por contratações, o setor de serviços seria um dos mais afetados com algumas das medidas estudadas.</p>
                            <p class="article">Pela proposta do Executivo, por exemplo, o PIS/Cofins seria substituído pela CBS (Contribuição Social sobre Operações com Bens e Serviços), que tem percentual orçado em 12%. Em média, o setor paga 4,5%, abaixo, portanto, do que indica a CBS, o que na prática resultaria em maior carga tributária</p>
                            <p class="article">Já a PEC 45 prevê a cobrança de um Imposto Sobre Bens e Serviços (IBS) de 30% em alíquota única. Mais uma vez, o setor de serviços seria bastante prejudicado, já que o IBS foi calculado de forma a não ser cumulativo. Nos setores industriais, por exemplo, é possível descontar o imposto pago em fases anteriores da produção. Para os serviços, em que o maior insumo consiste nos recursos humanos, não é possível a tomada desses créditos, haja vista não haver incidência sobre a folha de salários.</p>
                            <p class="article">Vale destacar que a Frente Parlamentar do Serviços vem tendo papel importante nesta discussão, com a apresentação de emendas que corrijam as distorções na área.</p>
                            <p class="article">Espera-se que os congressistas cheguem a uma solução equilibrada, ouvindo todos os lados da sociedade, sem que um setor seja mais prejudicado ou beneficiado.</p>
