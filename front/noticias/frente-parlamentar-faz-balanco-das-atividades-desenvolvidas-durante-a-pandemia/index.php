                            <h1>Frente Parlamentar faz balanço das atividades desenvolvidas durante a pandemia</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>23/10/2020 - 18h40</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">O Conselho Gestor da Frente Parlamentar em defesa do Setor de Serviços se reuniu nesta quarta, 21, com o presidente da Frente, deputado federal Laércio Oliveira para debater as ações desenvolvidas durante o ano. A representante da IGEOC, Mariane Schetter, apresentou um relatório das atividades desenvolvidas abordando temas como reforma tributária, desoneração da folha de pagamentos, parcelamento de tributos, Pronampe, Lei Geral da Proteção de Dados, Nexo Causal, entre outros assuntos.</p>
                            <p class="article">Preocupada com as propostas de reforma tributária que podem aumentar os impostos para o setor de Serviços, a Frente se mobilizou e realizou diversas reuniões para defender o setor com o ministro da Economia, Paulo Guedes, com o secretário Especial de Produtividade, Emprego e Competitividade Carlos da Costa, o Secretário Especial da Receita Federal José Tostes Neto, o Professor Marcos Cintra, o relator da Reforma Tributária Aguinaldo Ribeiro e com Alberto Macedo, do Movimento Simplifica Já. “Nós defendemos o emprego na Reforma Tributária. O setor de Serviços é o que mais emprega e representa 70% do PIB. Não pode sofrer aumento de impostos”, disse Laércio.</p>
                            <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2020/10/realizacoes-frente.png" width="450" height="auto" alt="Realizações Frente Parlamentar" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">A frente atuou também com ações para a desoneração da Folha de Pagamentos e o parcelamento de tributos federais. “Esse é um assunto de muita importância porque se não houvesse parcelamento, iria haver sobrecarga ou cumulatividade nos pagamentos”, afirmou a presidente da FENINFRA, Vivien Mello. “Com esse intuito tratamos sobre o tema em reuniões com autoridades do Poder Executivo, incluindo o Secretário Especial da Receita Federal, Tostes Neto, para sensibilizá-los da situação”, afirmou Mariane Schetter.</p>
                            <p class="article">Outro tema abordado na reunião foi a falta de regulamentação do Programa Nacional de Apoio às Microempresas e Empresas de Pequeno Porte (Pronampe) que dificultou o acesso ao crédito para as micro e pequenas empresas. Esse foi um dos temas tratados em reunião com o Secretário Especial da Receita Federal, José Tostes Neto. “O governo deve editar nos próximos dias uma Medida Provisória que irá liberar mais R$ 10 bilhões para o Pronampe. Os recursos virão da linha de financiamento de salários de pequenas e médias empresas que não teve tanto sucesso entre os empresários”, informou o relatório apresentado.</p>
                            <p class="article">Após o governo federal publicar a MP 927/2020 que previa que “os casos de contaminação pelo Coronavírus (covid-19) não seriam considerados ocupacionais, exceto mediante comprovação do nexo causal”, foram buscadas alternativas para regulamentar a matéria.</p>
                            <p class="article">Outro tema que a Frente Parlamentar atuou foi na Lei Geral de Proteção de Dados (LGPD). A Frente Parlamentar orientou as Entidades apoiadoras e também buscou alternativas de garantir maior segurança jurídica. Assim, foram realizadas algumas ações como atuação para a prorrogação do início da vigência para agosto de 2021 e a criação de uma parceria à ABNT para viabilizar a certificação de conformidade à LGPD.</p>
                            <p class="article">O presidente da FENASERHTT , Vander Morales, afirmou que é recém-chegado na Frente, mas está muito entusiasmado com os resultados até agora apresentados. “Eu não tinha dimensão do que foi feito. Quando integramos a Frente avaliamos que ela era capaz de aliar a técnica à parte política através do nosso deputado Laércio Oliveira. Ele é muito humilde, mas é o grande cabeça. Ele fala que tem uma participação pequena, mas a história parlamentar do deputado Laércio é riquíssima. Não é só por causa da lei do gás agora como relator não. Mas ele já enfrentou um desafio muito grande que foi a lei da terceirização, quando a terceirização era um palavrão em um governo de esquerda e ele teve que enfrentar muitos fake news. Foi uma batalha muito grande e o deputado teve uma participação brilhante, além de outras pautas”, afirmou Vander.</p>
                            <p class="article">Participaram ainda da reunião Antônio Totaro (FENINFRA), Armando Monteiro (ABRAMED e ANUP), Cláudio Tartarini (ABT), Edison Costa (ANEPS), João Marques (ANEPS) , Milena Oliveira (ANSEGTV), Paulo Rossi (ABAC), Ricardo Albregard (AMPRO), Hélzio Mascarenhas (FPS) e Soraya Chaves, Idelcio Magalhães e Bruna Mateus (OPS).</p>
