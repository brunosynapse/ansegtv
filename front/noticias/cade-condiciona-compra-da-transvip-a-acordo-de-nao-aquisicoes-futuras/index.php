                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">O Tribunal do Conselho Administrativo de Defesa Econômica (Cade) condicionou a aprovação da compra da Transvip pela Prosegur – uma das maiores empresas do ramo de transporte e custódia de valores no Brasil – ao compromisso da empresa de não fazer novas aquisições pelo prazo de três anos.<br> A decisão consta de um Acordo em Controle de Concentração (ACC).</p>
                            <p class="article">Os conselheiros referendaram, na quarta-feira (11/12), por unanimidade, o voto da relatora, Paula Farani de Azevedo Silveira, que acatou o posicionamento da Superintendência-Geral do órgão pela aprovação da aquisição da Transvip pela Prosegur, desde que firmado o acordo.</p>
                            <p class="article">Atualmente, a Prosegur detém entre 20% e 30% de participação no mercado no Rio de Janeiro e em São Paulo. Na avaliação do Cade, há um movimento de concentração no setor de transporte e custódia de valores, de forma que poderá ficar cada vez mais restrito nos próximos anos.  </p>
                            <p class="article">Apesar dessas considerações, a compra da Transvip pela Prosegur foi autorizada porque, na avaliação da relatora, a operação, por si só, não torna mais abrangente ou intensifica supostas estratégias de não-competição.</p>
                            <img src="../../img/uploads/2019/12/logo-cade-grande.jpg" width="490" height="auto" alt="Logo CADE - Conselho Administrativo de Defesa Econômica" class="pull-left m-2 img-fluid">
                            <p class="article">Presente à sessão de julgamento, o advogado José Del Chiaro, representante da Tecban, uma das interessadas, explicou que o setor está concentrado nas três maiores empresas – Prosegur, Brink’s e Protege que detêm, juntas, mais de 80% do mercado em todo o país.</p>  
                            <p class="article">“Essa é uma situação que traz uma preocupação muito grande, principalmente porque aquelas empresas que poderiam oferecer uma mínima resistência a esse oligopólio estão sendo adquiridas ou desaparecendo”, declarou Del Chiaro. De acordo com ele, o crescimento das líderes do setor se dá por meio da aquisição sumária dos menores concorrentes.</p>
                            <p class="article">Na exposição, Del Chiaro referiu-se também a um “fato novo”: o comunicado, na terça-feira (10/12), de encerramento das atividades da Esquadra no transporte e custódia de valores no Brasil.</p>
                            <p class="article">O advogado mencionou, ainda, estudos que apontam que, nas licitações públicas em que estão presentes apenas Prosegur, Brink’s e Protege, os preços são, em geral, 20% maiores do que aqueles praticados quando há mais participantes no certame. Da mesma forma, a quantidade de lances dados é muito menor nas situações em que as três maiores não encontram concorrência.</p>
