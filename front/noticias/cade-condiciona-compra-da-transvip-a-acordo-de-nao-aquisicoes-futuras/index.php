                            <h1>Cade condiciona compra da Transvip a acordo de não aquisições futuras</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>18/12/2019 - 15h30</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
