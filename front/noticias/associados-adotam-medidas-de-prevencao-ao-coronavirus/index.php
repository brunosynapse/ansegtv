                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article"><em>Diante da pandemia da COVID-19, as empresas associadas à ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) adotaram todas as medidas de proteção aos seus funcionários, prevenção contra a transmissão do vírus e manutenção dos serviços prestados à sociedade.</em></p>
                            <p class="article">A saúde e o bem-estar dos profissionais são as principais preocupações das empresas neste momento tão difícil em todo o mundo.</p>
                            <p class="article">Logo de início, nas funções em que foi possível, o home office foi implantado. Reuniões foram canceladas ou estão sendo realizadas virtualmente.</p>
                            <img src="../../img/uploads/2020/03/coronavirus.jpg" width="480" height="auto" alt="Coronavírus" class="pull-right m-2 img-fluid">
                            <p class="article">Viagens, eventos e treinamentos nacionais e internacionais foram adiados. Trabalhadores que retornaram do exterior passaram a ser monitorados e foram isolados pelo período de tempo recomendado pela OMS (Organização Mundial de Saúde).</p>
                            <p class="article">Nos ambientes corporativos, mesas foram afastadas umas das outras, os horários de uso dos refeitórios, copas e vestiários passaram a ser alternados, tudo para que sejam evitadas aglomerações.</p>
                            <p class="article">Funcionários com idade superior a 60 anos, gestantes, lactantes, portadores de doenças graves, além de trabalhadores que apresentarem sintomas de gripe ou que tiveram contato com pessoas diagnosticadas com o coronavírus estão recebendo os cuidados especiais indicados pelo Ministério da Saúde.</p>
                            <p class="article">Campanhas internas de prevenção também são divulgadas intensamente. A orientação é para que funcionários não compartilhem objetos de trabalho e providenciem a lavagem do uniforme com maior frequência.</p>
                            <p class="article">O álcool em gel é item obrigatório, tanto nas bases operacionais como nos veículos de transportes. Carros-fortes e leves são constantemente higienizados. Rotas são revisadas com a finalidade de otimizar e reduzir o tempo de interação entre os times.</p>
                            <p class="article">Locais de contato comum, como leitor biométrico e maçanetas de portas, tiveram a limpeza reforçada. Levantamentos sobre o número de casos de coronavírus em cada região de atuação das empresas são emitidos diariamente. Funcionários têm a temperatura medida para o controle de acesso às instalações da empresa.</p>
                            <p class="article">A parte tecnológica também é de vital importância. Notebooks e smartphones foram adquiridos, e a capacidade de internet foi ampliada para o trabalho remoto.</p>
                            <p class="article">Todas as medidas foram adotadas seguindo as mais rígidas regras de segurança, com o apoio da Polícia Federal e das seguradoras.</p>
                            <p class="article">Nada disso seria possível sem a integração e parceria entre todos os associados. Importante lembrar que o serviço de segurança privada, incluindo o transporte de valores, é considerado essencial e não pode ser suspenso, a exemplo de supermercados, hospitais, bancos, entre outras atividades.</p>
                            <p class="article">A ANSEGTV seguirá dando todo o apoio necessário aos seus associados, fortalecendo a sua importância para o setor de segurança e transporte de valores.</p>
