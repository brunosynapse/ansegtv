                            <h1>Reforma Tributária é tema de reuniões</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>03/07/2020 - 14h20</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Em sequência à série de reuniões para discutir propostas na área econômica, o presidente da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Gabriel Damasceno, e outros representantes do setor de serviços participaram nesta quinta-feira (2) de mais duas videoconferências.</p>
                            <p class="article">Mais uma vez, os encontros foram organizados pelo deputado federal Laércio Oliveira (PP-SE), líder da Frente Parlamentar em Defesa do Setor de Serviços. A Reforma Tributária esteve no centro dos debates.</p>
                            <p class="article">O primeiro, pela manhã, foi com o professor Marcos Cintra. Economista renomado, ele foi Secretário Especial da Receita Federal até o ano passado.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/07/congresso_agencia-senado_ansegtv.jpg" width="480" height="auto" alt="Congresso Nacional - Ansegtv" class=" img-fluid">	
                                <figcaption>Fonte: Agência Senado</figcaption>
                            </figure>
                            <p class="article">Na avaliação de Cintra, o governo deveria estudar formas de desonerar a folha de pagamentos. Ele não vê espaço para redução da carga tributária diante da previsão de déficit primário causado pela pandemia do novo coronavírus.</p>

                            <p class="article">À tarde, foi a vez do deputado federal Aguinaldo Ribeiro (PP-PB) ouvir os representantes do setor de serviços.</p>
                            <p class="article">Ribeiro disse que a discussão sobre a Reforma Tributária deverá ser retomada no segundo semestre com a volta dos trabalhos da Comissão Mista da Reforma Tributária, na qual é relator.</p>
                            <p class="article">Ele informou que está conversando com os presidentes da Câmara, Rodrigo Maia (DEM-RJ), e do Senado, Davi Alcolumbre (DEM-AP), para que as reuniões da Comissão ocorram de forma on-line. A votação da PEC ocorreria de forma presencial após o retorno das atividades nas duas Casas legislativas. A previsão é de que o Senado volte no início de agosto, já a Câmara, ainda não se sabe.</p>
                            <p class="article">O parlamentar destacou que a intenção é produzir uma PEC sem aumento do peso tributário para nenhum setor.</p>
                            <p class="article">Ribeiro também se demonstrou ainda ser favorável à proposta de desoneração da folha de pagamentos.</p>
