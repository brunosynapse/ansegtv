                            <h1>Associação criada para defender a livre concorrência e modernizar o setor de transporte de valores no país comemora primeiro aniversário</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>19/02/2020 - 12h58</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Difícil para a redação do site Livre Concorrência escrever sobre associações e sindicatos vinculados ao transporte de veículos novos sem os relacionar a práticas criminosas, como abuso de poder econômico, dominação de mercado, eliminação total ou parcial de concorrência, fixação artificial de preços, formação de cartel e associação criminosa.  Existem exceções, claro. Em outro setor, a Associação Nacional de Segurança e Transporte de Valores (Ansegtv) destaca-se por defender a livre concorrência. A entidade comemora neste mês o primeiro aniversário.</p>
                            <p class="article">Diferente das agremiações normalmente referidas aqui no site Livre Concorrência, a Ansegtv defende o livre mercado e a livre iniciativa. Foi criada para representar os interesses das pequenas e médias empresas que prestam serviços de segurança e transporte de valores.</p>  
                            <p class="article">Em 12 meses, reuniu 16 associadas, com 80 bases operacionais instaladas em 25 estados e no Distrito Federal.</p>
                            <img src="../../img/uploads/2020/02/Gabriel_TecBan.png" width="480" height="auto" alt="Gabriel - TecBan" class="pull-right m-2 img-fluid">
                            <p class="article">A Ansegtv surge em um momento em que o setor de transporte de valores enfrenta a concorrência desleal de transportadoras estrangeiras que começam a se expandir em território nacional. Nesse contexto, o presidente Gabriel Damasceno avalia os primeiros 12 meses da entidade:</p>
                            <p class="article">
                                <em>"A organização contribuiu de forma significativa para o aumento da qualidade, eficiência e segurança das operações de seus associados, incentivando a livre concorrência e se firmando como entidade representativa dos interesses das pequenas e médias empresas perante as diversas entidades do governo e da sociedade."</em>
                            </p>
                            <p class="article">Ele ressalta:</p>
                            <p class="article">
                                <em>"Temos um mercado muito importante para o funcionamento da economia e da sociedade, mas que precisa se modernizar com novos investimentos e com uma saudável desconcentração."</em>
                            </p>
                            <p class="article">E conclui:</p>
                            <p class="article">
                                <em>"A livre concorrência é uma condição primordial para que a segurança privada seja modernizada no Brasil."</em>
                            </p>
                            <p class="article">Quando criada, um dos objetivos principais da associação foi se colocar à disposição das autoridades para a troca de informações e fornecimento de subsídios que pudessem contribuir com a segurança das operações de transporte de valores no país. O apoio à Policia Federal – órgão regulador da atividade de segurança privada no país – nos esforços em relação à prevenção à lavagem de dinheiro também foi uma das ações executadas.</p>
                            <br>
                            <em>Fonte: Portal Livre Concorrência</em>
