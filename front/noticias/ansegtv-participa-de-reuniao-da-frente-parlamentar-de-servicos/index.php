                            <h1>ANSEGTV participa de reunião da Frente Parlamentar de Serviços</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>16/09/2020 - 17h41</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
