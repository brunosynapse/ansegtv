                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">A ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) participou no último dia 14 de setembro de reunião da Frente Parlamentar em Defesa do Setor de Serviços (FPS), cujo objetivo foi promover um balanço das atividades realizadas ao longo de 2020.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/09/frente-parlamentar.png" width="400" height="auto" alt="Logo Frente Parlamentar" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">Criada em 2019, a Frente é apartidária e reúne atualmente 210 deputados. O setor de serviços representa 70% do PIB e foi responsável apenas no ano passado por quase sete milhões de contratações.</p>

                            <p class="article">Neste ano, vale destacar as reuniões organizadas pela Frente, liderada pelo deputado federal Laercio Oliveira (PP-SE), que ajudaram a promover o diálogo entre setores empresariais de serviço e o governo, em um período tão delicado e instável, originado pela pandemia do novo coronavírus.</p>
                            <p class="article">Esses encontros foram importantes para apresentar as demandas do setor de serviços e, ao mesmo tempo, ouvir as propostas do Executivo.</p>
                            <p class="article">Na reunião desta segunda, um dos temas foi o lançamento do site da Frente <a href="https://frenteparlamentarservicos.com.br" target="_blank">(https://frenteparlamentarservicos.com.br)</a>. Um dos destaques da página é o espaço “Think Tank”, no qual os apoiadores do movimento podem sugerir discussões e análises técnicas que colaborem com o desenvolvimento do setor de serviços.</p>
                            <p class="article">O site ainda reúne informações sobre os associados, notícias e as proposições legislativas de relevância para o segmento.</p>
