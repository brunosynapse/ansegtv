                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Representantes das empresas filiadas à ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) participaram nesta quinta-feira (18) de uma live transmitida pelo YouTube no canal do CT Segurança.</p>
                            <p class="article">Os associados discutiram os principais desafios enfrentados diante da pandemia da COVID-19, compartilharam experiências e soluções que ajudaram a amenizar os efeitos da chegada do coronavírus ao país.</p>
                            <p class="article">Além do presidente da Associação e superintendente da TBForte, Gabriel Damasceno, estiveram presentes: Ana Rita Oliveira (ASO), Dyego Salaro (Blue Angels), Edson de Araujo Jr. (Federal Segurança), Eginaldo Oliveira (Corpvs), Elizeu Ivo da Costa (Valorsat), Fábio Weyhrother (Renaforte), Fábio Torquato (Kairós), Michele de Sousa (Kairós), Max Madruga (TBForte), Pedro Henrique da Silva (Fenixx), Roberto Cruz (Brasifort) e Victor Serrano (Global Service).</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/06/live_ansegtv_jun2020.jpg" width="480" height="auto" alt="Live ANSEGTV" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">A advogada Lirian Cavalhero, que vem garantindo todo suporte jurídico ao grupo nesta fase, também participou, esclarecendo dúvidas e pontuando as principais mudanças na legislação.</p>

                            <p class="article">Durante a reunião, todos elogiaram a capacidade de reinvenção, de adaptação e resiliência dos colaboradores. Sem isso, não seria possível enfrentar os obstáculos surgidos.</p>
                            <p class="article">Para os associados, o pior momento foi entre o fim de março e o início de abril, quando as informações sobre o novo coronavírus ainda eram poucas. O desconhecimento deixou muitos funcionários temerosos.</p>
                            <p class="article">Após esse período, no entanto, a situação começou a se estabilizar. Medidas de prevenção, campanhas de conscientização e o suporte de especialistas em saúde ajudaram a controlar o número de casos. O trabalho em home office se mostrou uma grande ferramenta, até aumentando a produtividade.</p>
                            <p class="article">Protegidos, com máscaras e a correta higienização de veículos, trabalhadores da linha de frente puderam ir às ruas, já que as atividades de segurança e transporte de valores são essenciais para o país e não podem parar. Clientes também foram compreensivos com algumas mudanças, como remanejamentos de rotas e horários.</p>
                            <p class="article">As medidas emergenciais anunciadas pelo governo ajudaram a conservar empregos. Gabriel Damasceno ressaltou que o objetivo principal sempre foi passar tranquilidade a todos funcionários e manter a qualidade dos serviços prestados.</p>
                            <p class="article">Ao final da reunião, o presidente da ANSEGTV lembrou que a batalha ainda está em andamento, mas com a união entre todos os associados será possível atravessar este momento tão conturbado.</p>
