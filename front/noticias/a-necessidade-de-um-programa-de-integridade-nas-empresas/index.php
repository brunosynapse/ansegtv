                            <h1>A necessidade de um Programa de Integridade nas empresas</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>20/11/2020 - 12h30</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Qualquer empresa, atuando em qualquer segmento de negócio, de qualquer porte e em qualquer patamar de faturamento, está sujeita à regulamentação legal.</p>
                            <p class="article">No Brasil, encontram-se em plena vigência leis que afetam todas as empresas, normatizando temas como a proteção de dados, a conduta concorrencial, a prevenção contra a corrupção e a lavagem de dinheiro. Por esse motivo, é urgente que as empresas implantem em suas estruturas um Programa de Integridade.</p>
                            <p class="article">Pode parecer que estamos falando de algo novo, mas isso não é verdade. Basta lembrarmos que um dos mais famosos chefões da máfia nos Estados Unidos, Al Capone, apesar de todos os crimes cometidos e ordenados, foi condenado a 11 anos de prisão e multa de US$ 50 mil por sonegar imposto, já no final do ano de 1931. Esse mesmo crime já está previsto na nossa legislação desde julho de 1965, quando foi definido pela Lei 4.729.</p>
                            
                            <p class="article">Mas afinal, o que é um Programa de Integridade? Podemos extrair uma boa definição conceitual através da leitura do art. 41 do Decreto no 8.420/2015: <em>“Programa de integridade consiste, no âmbito de uma pessoa jurídica, no conjunto de mecanismos e procedimentos internos de integridade, auditoria e incentivo à denúncia de irregularidades e na aplicação efetiva de códigos de ética e de conduta, políticas e diretrizes com objetivo de detectar e sanar desvios, fraudes, irregularidades e atos ilícitos”.</em></p>
                            <p class="article">O planejamento da empresa para implantação de um Programa de Integridade deve ter por base toda a legislação vigente sobre os temas descritos acima, sendo as principais: Lei nº 9.613/1998 (Prevenção à Lavagem de Dinheiro), Lei nº 12.529/2011 (Defesa da Concorrência – Lei Antitruste), Lei nº 12.846/2013 (Lei Anticorrupção) e Lei nº 13.709/2018 (Lei Geral de Proteção de Dados Pessoais).</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/11/max-tavares.png" width="450" height="auto" alt="Max Tavares" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">Um bom Programa de Integridade deve ser planejado para realizar de forma permanente e cíclica três grandes funções: prevenção, detecção e reação.</p>
                            <p class="article">A função de prevenção deve ser exercida através do aculturamento de toda a empresa em relação aos seus valores e políticas e, para isso, nada melhor do que uma comunicação eficiente e treinamentos constantes para os funcionários (e toda a cadeia de stakeholders deve ser envolvida nesse processo – os stakeholders são as pessoas e as organizações que serão afetadas, de forma direta ou indireta, positiva ou negativamente).</p>
                            <p class="article">A função de detecção deve ter o apoio de uma área especializada na empresa, que normalmente se chama de Governança Corporativa ou Gestão de Riscos e Compliance, responsável pela gestão do programa e pelo gerenciamento de riscos, aliada aos processos de controles internos e auditorias independentes, além do suporte dos canais de comunicação implantados, para recebimento dos alertas sobre possíveis violações.</p>
                            <p class="article">Por fim, a função de reação, que deve contemplar um plano de ação para cada inconformidade encontrada, sempre considerando a previsão legal e os regramentos internos.</p>
                            <p class="article">Dito isso, fica muito clara a necessidade de apoio jurídico na implementação e acompanhamento do Programa de Integridade. Sem a avaliação jurídica necessária e o aconselhamento nas decisões corporativas, a empresa pode virar um “barco à deriva” no mundo empresarial e colocar em risco todo o investimento realizado no negócio.</p>
                            <p class="article">Mas lembre-se, é urgente a necessidade das empresas implantarem um Programa de Integridade. Sucesso nesse desafio!</p>
                            <p class="article">
                                <em>Max Tavares Madruga, advogado e diretor jurídico da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores).</em>
