                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">A Comissão de Fiscalização e Controle (CTFC) adiou na terça-feira (17/12) a votação do projeto substitutivo da Câmara dos Deputados 6/2016, que estabelece o Estatuto da Segurança Privada e da Segurança das Instituições Financeiras. De autoria do então senador Marcelo Crivella, o projeto original tinha por objetivo único dar aos vigilantes um piso nacional de salário, reajustado anualmente. No entanto, a proposta recebeu 117 sugestões de emendas na Câmara, o que ampliou a sua abrangência, tornando-o na prática um estatuto. As informações são da Agência Senado.</p>
                            <img src="../../img/uploads/2019/12/plenario-do-senado-federal.png" width="490" height="auto" alt="Sessão de votação no Senado Federal" class="pull-left m-2 img-fluid">
                            <p class="article">Composto por 72 artigos, divididos em 11 capítulos, o texto é relatado na CTFC pelo senador Randolfe Rodrigues (Rede-AP). Com o pedido de mais tempo para avaliação da matéria pelos demais parlamentares, a votação então ficou para 2020. Senadores como Paulo Rocha (PT-PA) e Major Olimpio (PSL-SP) destacaram a necessidade dessa legislação e cobraram o compromisso da comissão em votar o texto assim que o Congresso retornar do recesso.</p>
                            <p class="article">A proposta pretende disciplinar a necessidade de autorização prévia para funcionamento de empresas e a fiscalização, pela Polícia Federal, dos serviços de segurança privada e do plano de segurança em dependências de instituições financeiras. Além disso, o texto trata do funcionamento das escolas de formação; do uso de produtos controlados de uso restrito, de armas de fogo e de menor potencial ofensivo; da prestação do serviço em espaços de uso comum, transportes coletivos, estabelecimentos prisionais, portos e aeroportos, estabelecimentos públicos e privados e áreas públicas; dos requisitos para exercício profissional, bem como do direito a seguro de vida, assistência jurídica e piso salarial fixado em acordos e convenções coletivas, que também podem ajustar a jornada de trabalho.</p>
