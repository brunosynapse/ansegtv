                            </figure>
                            <p class="article">Isso só foi possível graças aos esforços de governo e transportadoras de valores. A pandemia trouxe desafios, mas as empresas souberam se adaptar e mantiveram o serviço de qualidade.</p>

                            <p class="article">Com a crise econômica, a tendência é que a demanda pelo numerário se mantenha e até cresça. Em momentos como agora, por exemplo, o entesouramento aumenta. Ou seja, receosa sobre o futuro, a população saca e guarda o seu dinheiro.</p>
                            <p class="article">Ainda é preciso aguardar maiores detalhes pelo Planalto sobre a introdução da nova cédula no mercado, mas a ANSEGTV e suas associadas avaliam como positiva a medida e estarão preparadas para seguir atendendo a todos.</p>
