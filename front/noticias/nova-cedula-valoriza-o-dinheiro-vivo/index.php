                            <p class="article">Muito se discute atualmente a respeito do crescimento de pagamentos virtuais em detrimento da utilização do dinheiro em espécie.</p>
                            <p class="article">Impossível ignorar os benefícios trazidos pela tecnologia, mas o anúncio pelo Banco Central da nova cédula de R$ 200 mostra que o dinheiro vivo segue com vital importância.</p>
                            <p class="article">De março até julho, segundo a autarquia federal, a quantidade de cédulas em circulação subiu de R$ 216 bilhões para R$ 277 bilhões, um aumento de 29% e recorde da série histórica iniciada em dezembro de 2001.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/07/cedula_200_bancocentral.png" width="480" height="auto" alt="Cédula 200 reais" class=" img-fluid">	
