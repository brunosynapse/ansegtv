    public function show(int $id)
    {
        $post = Post::find($id); //refatorar isso que tÃ¡ uma bosta e a injecao de dependencia nao esta funcionaod......

        if(!$post){
            abort(404);
        }

        if (!Cookie::has($post->id) && !Auth::check()) {
            Cookie::queue($post->id, 'counter-views', 6 * 60);
            $post->views += 1;
            $post->save();
        }

        return view('site.pages.posts.show', compact('post'));
    }

