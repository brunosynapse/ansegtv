                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Em reunião no último dia 3 de abril, o CONFAZ (Conselho Nacional de Política Fazendária) e a Secretaria da Receita Federal decidiram por ajustar o artigo 199 do Código Tributário Nacional (Lei nº 5.172, de 25 de outubro de 1966. Tal artigo trata da fiscalização tributária pela União, estados, Distrito Federal e municípios.</p>
                            <p class="article">Ficou assim estabelecida a instituição da GTV-e (Guia de Transporte de Valores Eletrônica), modelo 64, que deverá ser emitida pelos contribuintes do ICMS que realizarem transporte de valores.A alteração foi publicada na edição de 7 de abril do Diário Oficial da União. O ajuste entrou em vigor nesta data e produz efeitos a partir de 1º de setembro de 2020.O documento substitui a antiga GTV (Guia de Transporte de Valores) e o Extrato de Faturamento, que haviam sido instituídos em 22 de agosto de 1989. Dentre outras normas, a GTV-e deverá conter os dados que discriminam a carga: quantidade de volumes/malotes, espécie do valor (numerário, cheques, moeda, outros) e valor declarado de cada espécie.</p>
                            <img src="../../img/uploads/2020/05/confaz.png" width="480" height="auto" alt="CONFAZ" class="pull-right m-2 img-fluid">
                            <p class="article">Para a assinatura digital, deverá ser utilizado certificado digital emitido dentro da cadeia de certificação da Infraestrutura de Chaves Públicas Brasileira - ICP-Brasil, que contenha o CNPJ de qualquer dos estabelecimentos do contribuinte, a fim de garantir a autoria do documento digital. A concessão da Autorização de Uso da GTV-e será feita pela administração tributária competente, que analisará, dentre outros requisitos, a regularidade fiscal e o credenciamento do emitente.</p>
                            <p class="article"> Com a implementação do dispositivo, vários departamentos das empresas de transporte deverão ser beneficiados, como os de Contabilidade, Operações, Risco e Segurança, Gestão de Recebimento, dentre outros. A nova ferramenta possibilitará ainda a padronização de informações, uma maior integração com a SEFAZ (Secretaria de Estado da Fazenda), a diminuição de custos, a redução do retrabalho, assertividade no processo de cobrança e a possibilidade de acesso de informações em tempo real.</p>
                            <p class="article"> O presidente da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Gabriel Damasceno, avalia como altamente positiva a instauração do novo documento.“Defendemos a modernização da legislação e dos processos fiscais gerando mais eficiência e produtividade para todos os envolvidos”, afirma.
                            </p>
                            <p class="article">
