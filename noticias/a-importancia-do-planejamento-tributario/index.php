                            <p class="article">Ao que tudo indica, as sessões em plenário virtual contribuíram para acelerar os julgamentos das pautas tributárias no STF (Supremo Tribunal Federal), que vem priorizando a matéria. Em agosto, foram analisados vários temas relevantes. Foi decidido, por exemplo, que é inconstitucional a incidência de contribuição previdenciária sobre o salário-maternidade, pago durante o período de licença.</p>
                            <p class="article">Estavam também incluídas na agenda do mês a ação que busca o reconhecimento da inexigibilidade das contribuições para o SEBRAE, a APEX e a ABDI, e outra, a respeito das contribuições destinadas ao Incra, ambas a partir da vigência da Emenda Constitucional nº 33/2001. As duas foram retiradas de pauta a pedido do Ministro Gilmar Mendes e ainda não têm data para retomada de análise.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/redimensionadas/017.png" width="480" height="auto" alt="andrey Ferreira - Advogado Especialista em Direito Empresarial" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">A inclusão em pauta de julgamento de outros casos, como a tese que defende a inconstitucionalidade do ISS na base de cálculo do PIS e da COFINS e a possibilidade de exclusão da CPRB – Contribuição Previdenciária substitutiva sobre a Receita Bruta da base de cálculo do PIS e da Cofins, pode deixar claro ao legislador as limitações da aplicabilidade de impostos “calculados por dentro”.</p>
                            <p class="article">Tanto é assim que o projeto de lei encaminhado pelo Executivo ao Congresso, que cria a Contribuição sobre Operações com Bens e Serviços (CBS), em substituição ao PIS/Pasep e à Cofins, prevê que o ICMS e o ISS não integrarão a base de cálculo do novo tributo. E nem a própria CBS integrará a sua base de cálculo, ao contrário do que ocorre atualmente com o PIS/Pasep e a Cofins.</p>
                            <p class="article">A agitada agenda tributária do STF, aliada ao momento de instabilidade econômica, vem gerando uma verdadeira corrida dos contribuintes a ingressarem com ações judiciais na defesa de seus direitos. Isso ocorre porque os afetados pela repercussão geral, e com possibilidade de aplicação da modulação dos efeitos, só terão direito a restituir ou compensar os tributos pagos indevidamente a maior nos últimos cinco anos se já tiverem ingressado com a ação até o momento do julgado.</p>
                            <p class="article">Neste cenário, também estão incluídas as oportunidades previdenciárias, como a redução de riscos, incluindo passivos de contribuições para o INSS e os decorrentes de acidentes do trabalho. Os próximos meses devem ser ainda mais pesados para as empresas. Com o fim de diferimentos de impostos e a primeira parcela do 13º salário a ser depositada em novembro, os esforços precisarão ser redobrados e a capacidade de planejamento será mais uma vez colocada à prova. Importante lembrar o compromisso assumido pelo governo de não elevar a carga tributária. Em recente audiência na Comissão Mista da Reforma Tributária do Congresso Nacional, o ministro da Economia, Paulo Guedes, afirmou que, se a arrecadação federal aumentar com a CBS em 12%, a alíquota proposta pode baixar para 10%, 9% ou 8%.</p>
                            <p class="article">Vale indagar se o ministro considera que os contribuintes e especialistas já contam com o fato de que suas alíquotas efetivas diminuíram, uma vez que o PIS e a COFINS já não possuem o malfadado “cálculo por dentro”.</p>
                            <p class="article">A proposta de Reforma Tributária do governo traz simplificações relevantes, maior segurança jurídica aos contribuintes, com importante redução do contencioso, além de uma agilização do fluxo de caixa mediante devolução mais rápida dos créditos acumulados. No entanto, caso se confirme a alíquota única de 12%, mostra-se ainda mais necessária a desoneração da folha de pagamentos.</p>
                            <p class="article"><em>Andrey Ferreira é advogado, especialista em Direito Empresarial e assessor tributário da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores).</em></p>
