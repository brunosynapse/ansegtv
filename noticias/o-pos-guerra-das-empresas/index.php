                            <h1>O “pós-guerra” das empresas</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>17/07/2020 - 17h</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Na década de 1960, Leon C. Megginson, professor da Universidade de Louisiana, nos Estados Unidos, adaptou uma ideia da Teoria da Evolução das Espécies para explicar o sucesso de algumas empresas no mercado. Em escrito de 1963, tratando da recuperação europeia no pós-guerra — “Lessons from Europe for American Business” —, emulou o conceito darwiniano de que não são os mais fortes que sobrevivem, mas os que se adaptam às mudanças do ambiente. Quase 60 anos depois, as empresas brasileiras, já combalidas por seguidas crises econômicas de origem interna e externa, põem essa comparação à prova ao ter sua resiliência testada pelo novo coronavírus.</p>
                            <p class="article">Até o fim de 2020, a pandemia da COVID-19 deve se tornar a causadora da maior depressão econômica da história recente. A estimativa de perdas calculada pelo Banco Mundial, divulgada em março pela Confederação Nacional dos Serviços, foi de que o primeiro semestre veria uma retração de 6% no PIB mundial, o equivalente a 5,3 trilhões de dólares e 212,8 milhões de novos desempregados — mais de 6% da força de trabalho do planeta.</p>
                            <p class="article">No Brasil, o mercado projeta uma queda de até 6,5% do PIB em 2020. Segundo dados da Pnad Covid-19, publicada pelo IBGE, o desemprego no país cresceu 10,8% em maio. Isso significa 1,05 milhão de desempregados a mais entre a primeira e a última semana do mês. Os números ainda estão sendo levantados, mas até o fim do primeiro semestre, só o setor de serviços pode ter sido responsável por um declínio econômico de 3,4%, e por 2,3 milhões de demissões, segundo a CNS.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/redimensionadas/009.png" width="480" height="auto" alt="Gabriel Tecban" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">Uma boa notícia, no entanto, foi a recente conversão em lei da Medida Provisória 936, postergando a possibilidade de suspensão de contratos de trabalho e as reduções de jornada e de salários, bem como a edição de decreto que ampliou os prazos destas medidas. Porém, espera-se ainda que haja a prorrogação da desoneração da folha de pagamentos – segundo dados da Confederação Nacional de Tecnologia da Informação e da Comunicação (Contic), o número de demissões pode chegar a um milhão nos 17 setores afetados pelo recente veto do Governo Federal a esta prorrogação.</p>

                            <p class="article">Ainda como forma de manter empregos e o reaquecimento da economia, mostra-se de vital importância a revisão reforma tributária em tramitação no Congresso Nacional, que sobrecarrega o setor de serviços, responsável por 60% do PIB. Simulações, como a do tributarista Hamilton Dias de Souza, mostram que o aumento da carga para empresas optantes pelo lucro presumido pode chegar a 211% se aplicadas as regras previstas na PEC 45 — justamente no setor que mais emprega e no pior momento possível, em que o desemprego ameaça destruir o que a pandemia tiver poupado.</p>
                            <p class="article">As pequenas e médias empresas do setor de transporte de valores vêm fazendo sua parte. Não demitiram, não fecharam e têm mantido a qualidade de seus serviços.</p>
                            <p class="article">Campanhas de conscientização e de prevenção ao novo coronavírus, constante higienização de sedes e veículos, remanejamento de equipes a fim de proporcionar um maior distanciamento social, revisão de rotas e o home office em funções administrativas foram medidas essenciais que permitiram a continuidade das atividades.</p>
                            <p class="article">Isso não quer dizer que as empresas não tenham sentido o golpe. Mas sim que estão se adaptando à nova realidade, investindo em melhoria de processos, aumento de produtividade e eficiência e também usando dos recursos colocados à disposição pelo poder público. </p>
                            <p class="article">Outro importante movimento do governo federal foi a promessa de estímulo de R$ 147 bilhões à economia. Não só via crédito às empresas, mas também pelo auxílio-emergencial pago às famílias. O incentivo ao consumo terá evitado até o fim do ano, segundo estudo da Universidade Federal Rural do Rio de Janeiro (UFRRJ), uma queda de 2 pontos percentuais a mais no PIB do que sem a ajuda.</p>
                            <p class="article">Neste contexto, vale recordar a importância das transportadoras de valores. Grande parte da população ainda usa o dinheiro para pagamento de contas ou compras.  Ou seja, a moeda é questão de sobrevivência para muitos comerciantes, trabalhadores informais e, principalmente, a população mais carente.</p>
                            <p class="article">É prematuro pensar em 2021 como data da retomada dos negócios em parâmetros pré-pandemia. Temos um longo trajeto até a recuperação plena da economia e medidas que auxiliem as empresas a continuar respirando são tudo o que separa a sobrevivência de algumas delas da extinção dos empregos no país.</p>
                            <p class="article"><em>Gabriel Damasceno é presidente da Associação Nacional de Segurança e Transporte de Valores (ANSEGTV)</em></p>
