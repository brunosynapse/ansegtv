                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Em meio a tantas incertezas e dificuldades, a liberação pela Anvisa (Agência Nacional de Vigilância Sanitária) do uso emergencial das primeiras vacinas contra a Covid-19 no Brasil trouxe esperança e otimismo.</p>
                            <p class="article">A ANSEGTV acompanha com atenção os desdobramentos da medida e defende a campanha de imunização. A ordem estipulada pelas autoridades deve ser respeitada. A população mais vulnerável, idosos e profissionais da linha de frente do combate à pandemia devem e precisam ser vacinados antes que os demais.</p>
                            <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/01/vacina-covid.png" width="450" height="auto" alt="Pessoa preparando a vacina do COVID" class=" img-fluid">	
                                <figcaption>Tânia Rego/Agência Brasil</figcaption>
                            </figure>
                            <p class="article">É necessário também debater alternativas para agilizar e acelerar a proteção dos profissionais envolvidos em outras atividades essenciais.</p>
                            <p class="article">Profissionais do setor de transportes foram incluídos recentemente no grupo prioritário para receber a vacina. Segundo o governo, estão nesta lista caminhoneiros, portuários, funcionários de companhias aéreas, metroferroviários, entre outros. Não há ainda data definida para a imunização dessas categorias.</p>
                            <p class="article">Importante lembrar o papel das transportadoras de valores na pandemia. Principalmente com o auxílio emergencial, a circulação de dinheiro e o abastecimento de caixas eletrônicos foi essencial.</p>
                            <p class="article">Considerado serviço essencial, o setor nunca parou e pôde ajudar a população em um período tão conturbado.</p>
                            <p class="article">Empresas estão dispostas a colaborar com os governos na aquisição dos medicamentos. Sem uma vacinação em massa, célere, o processo de retomada das atividades e de criação de empregos tende a levar mais tempo.</p>
                            <p class="article">O setor de serviços, por exemplo, vem dando mostras de recuperação. Em novembro de 2020, mesmo em meio ainda a tantas dificuldades, o segmento cresceu 2,6% frente a outubro, segundo o IBGE.</p>
                            <p class="article">Uma ampla imunização, sem dúvidas, ajudaria a alavancar e acelerar ainda mais essa recuperação.</p>
