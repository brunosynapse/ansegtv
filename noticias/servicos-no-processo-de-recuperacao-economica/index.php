                            <h1>Serviços no processo de recuperação econômica</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>11/11/2020 - 19h20</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Dados de setembro do Ministério da Economia mostram que, pelo segundo mês seguido, o país criou empregos formais. Foram abertos 313.564 postos com carteira assinada, o melhor resultado para o período desde o início da série histórica do Caged, em 2010. Por outro lado, segundo o <a href="https://agenciadenoticias.ibge.gov.br/agencia-noticias/2012-agencia-de-noticias/noticias/29321-desemprego-chega-a-14-4-no-trimestre-encerrado-em-agosto" target="_blank">IBGE</a>, a taxa de desemprego chegou a 14,4% no trimestre terminado em agosto, aumento de 1,6 ponto percentual frente ao trimestre encerrado em maio (12,9%), o que mostra que o caminho para recuperação da economia será longo.</p>
                            <p class="article">O setor de serviços, o que mais emprega no país, foi um dos mais afetados pela pandemia. Medidas do governo federal, como o programa de suspensão de contrato e redução de jornada e o auxílio emergencial, foram importantes para evitar perdas ainda maiores. O país, no entanto, necessita de mudanças mais amplas e duradouras, que possibilitem uma retomada sólida e expressiva.</p>
                            <p class="article">A política tem papel decisivo neste cenário. Atualmente, as eleições municipais acabam por atrair boa parte das atenções e esforços de congressistas. A pauta legislativa, porém, não pode ficar em segundo plano.</p>
                            
                            <p class="article">Em meio à crise econômica, a Frente Parlamentar do Serviços vem desempenhando importante papel. No total, o grupo realizou mais de 60 reuniões com autoridades, para debater e sugerir propostas. Ocorreram encontros importantes com autoridades, como o ministro da Economia, Paulo Guedes, o secretário especial de Produtividade, Emprego e Competitividade, Carlos da Costa, o secretário especial da Receita Federal José Tostes Neto, o secretário especial de Previdência e Trabalho, Bruno Bianco, entre outros.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/11/gabriel-perfil.jpeg" width="450" height="auto" alt="Presidente da ANSEGTV - Gabriel Damasceno" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">Como não poderia deixar de ser, um dos temas de maior preocupação é a Reforma Tributária, discutida em 16 dessas reuniões. Atualmente, tramitam no Congresso duas PECs (110 e 45/2019) sobre o assunto, além do Projeto de Lei 3.887/2020, do governo federal. As propostas são positivas ao extinguirem e unificarem impostos, além de trazerem maior segurança jurídica e transparência. Por outro lado, ainda sobrecarregam sobremaneira o setor de serviços, com incidência ampla de tributação sobre o consumo e poucas opções de créditos a compensar.</p>
                            <p class="article">A falta de regulamentação do Programa Nacional de Apoio às Microempresas e Empresas de Pequeno Porte (Pronampe), que dificultou o acesso ao crédito para as micro e pequenas empresas, foi um dos temas tratados em reunião com o secretário Tostes Neto. É de vital importância que o governo estude mais medidas que visem o auxílio a negócios afetados pela crise sanitária.</p>
                            <p class="article">O nexo causal, que é a comprovação da doença causada pela atividade profissional, ainda gera dúvidas para os casos de Covid-19. Projetos de lei em andamento atingem diretamente o setor de serviços. Com o desemprego ainda em alta, a judicialização na área deve crescer no período pós-crise. Assunto foi debatido em encontro com o secretário Bruno Bianco</p>
                            <p class="article">Outro tema que a Frente atuou foi na Lei Geral de Proteção de Dados (LGPD). Parlamentares orientaram as entidades apoiadoras e também buscaram alternativas para garantir maior segurança jurídica às empresas.</p>
                            <p class="article">Ações para a desoneração da folha de pagamentos e o parcelamento de tributos federais foram amplamente discutidas com o secretário Tostes Neto. A prorrogação do benefício até dezembro de 2021, aprovada pelo Congresso, evitará um aumento de custos para as empresas e cortes de postos de trabalho.</p>
                            <p class="article">Diante do atual cenário, fica claro que uma melhora efetiva da economia só virá da coesão entre governo, parlamentares e empresas.</p>
                            <p class="article">
