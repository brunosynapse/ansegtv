                            <h1>Cade propõe à Brink's não realizar aquisições por três anos</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>20/02/2020 - 14h55</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
