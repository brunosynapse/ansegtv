                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">O Conselho Administrativo de Defesa Econômico (CADE) condicionou, em decisão tomada nesta quarta-feira (19/02), a compra da Tecnoguarda pela norte-americana Brink's à celebração de um Acordo de Controle em Concentrações (ACC) que proíbe, pelo prazo de três anos, novas aquisições de empresas que oferecem serviços de transporte de valores no Brasil.</p>
                            <p class="article">O ato de concentração foi aprovado por meio de voto de minerva do presidente, Alexandre Barreto de Souza – após empate entre os ministros favoráveis e contrários à matéria – apesar de parecer da Superintendência-Geral do Cade que recomendava o veto ao negócio.</p>  
                            <p class="article">As conselheiras Paula Farani de Azevedo Silveira e Lenisa Rodrigues Prado votaram pela reprovação da aquisição da Tecnoguarda pela Brink's, com o argumento de que, nesse caso – diferentemente daquele que envolveu, no final do ano passado, a Prosegur e a Transvip – o ACC não poderá para impedir a concentração no setor.</p>
                            <p class="article"><em>“O remédio proposto se mostra inadequado para sanar a totalidade das preocupações nesse caso e não deve ser replicado por não se mostrar suficiente para preservar os mercados geográficos afetados, já que, na presente operação, não se constata eventuais rivais que possam se desenvolver”</em>, explicou Paula Farani.</p>
                            <img src="../../img/uploads/2020/02/sede_cade.png" width="480" height="auto" alt="Sede do Cade" class="pull-right m-2 img-fluid">
                           <p class="article">
                           O conselheiro Sergio Costa Ravagnani também se posicionou contra a aprovação irrestrita ao ato de concentração, sugerindo um posicionamento intermediário, que previa o desinvestimento em Mato Grosso.
                            </p>
                            <p class="article">
                            Já os conselheiros Luis Henrique Bertolino Braido e Alexandre Barreto de Souza –  apesar de reconhecerem os problemas gerados pela operação – acompanharam a posição do relator, Mauricio Oscar Bandeira Maia, o qual já havia negociado com as partes a celebração do ACC.
                            </p>
                            <p class="article">
                            "Notamos alguma resistência do CADE em alterar a jurisprudência que vinha se consolidando", declarou o  advogado José Del Chiaro, representante da Tecban, uma das interessadas.
                            </p>
                            <p class="article">O propósito do termo, a ser firmado pela Brink's como requisito para a efetivação da compra da Tecnoguarda, é impedir que a operação afete, negativamente, a concorrência do mercado de carros-fortes no Centro-Oeste. Após os três anos, a gigante do setor também terá de informar ao Cade, nos próximos dois anos, a realização de quaisquer novas aquisições.</p>
