                            <h1>Conselho Gestor da Frente Parlamentar do Serviços discute conquistas e planos para 2021</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i> 15/12/2020 - 15h30</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
