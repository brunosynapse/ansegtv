                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">O Conselho Gestor das entidades apoiadoras da Frente Parlamentar do Serviços realizou sua última reunião do ano nesta segunda-feira (14). Em formato online, o encontro contou com a participação do presidente da ANSEGTV, Gabriel Damasceno, entre outros representantes de instituições.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/12/frente-parlamentar-logo.png" width="450" height="auto" alt="Logo - Frente Parlamentar" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">No encontro, foi apresentado um balanço das atividades do ano e os principais desafios para 2021. Dentre estes, alguns prioritários, como a tramitação da Reforma Tributária, a desoneração da folha de pagamentos, o possível parcelamento de débitos tributários, a regulamentação do trabalho em home office, a discussão sobre o nexo causal e o maior engajamento institucional da Frente, com o preenchimento das coordenações micro e macrossetoriais, por deputados integrantes da Frente.</p>
                            <p class="article">Em sua fala, Gabriel agradeceu a todos os participantes, colocou a ANSEGTV à disposição dos demais apoiadores e destacou os avanços conquistados ao longo do ano, mesmo diante das dificuldades impostas pela pandemia. “Temas como redução proporcional da jornada e salário, suspensão do contrato de trabalho, acesso ao crédito, entre outros assuntos, foram essenciais para a continuidade, quiçá, a sobrevivência de muitas empresas desse país. O ano de 2021 vai nos exigir ainda mais empenho no tema da Reforma Tributária e em outros assuntos relevantes na agenda trabalhista, como débitos e execuções, Covid como doença ocupacional, teletrabalho, e outros assuntos que venham a surgir”, afirmou.</p>
