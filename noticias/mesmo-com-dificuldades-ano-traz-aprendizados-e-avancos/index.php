                            <h1>Mesmo com dificuldades, ano traz aprendizados e avanços</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>01/07/2020 - 12h</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Ao final de 2019, a expectativa para 2020 era grande. Os desafios que se avizinhavam eram muitos, mas ninguém poderia imaginar um ano tão atribulado quanto o que estamos vivendo. Passado o primeiro semestre, é hora de fazer um balanço sobre as principais iniciativas da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores).</p>
                            <p class="article">Antes do mundo ser alvejado pela pandemia do novo coronavírus, vale recordar a importante parceria com a Polícia Federal na elaboração do Manual de Prevenção à Lavagem de Dinheiro. O presidente da associação, Gabriel Damasceno, esteve em Brasília, em fevereiro, na sede da Coordenação-Geral de Controle de Serviços e Produtos (CGCSP/PF) para participar da entrega do documento. O guia lista as obrigações e medidas de prevenção a serem adotadas pelo mercado de transporte de valores, especialmente as comunicações a serem feitas pelas empresas ao COAF (Conselho de Controle de Atividades Financeiras).</p>
                            <p class="article">Já em março, quando governos de diversos estados instituíram a quarentena e os primeiros casos da nova doença surgiram no país, foi preciso adaptar diversos mecanismos e condutas. A principal preocupação de todos os associados sempre foi a saúde e o bem-estar de seus colaboradores. Home office, campanhas de conscientização, reorganização operacional e higienização constante de ambientes de trabalho entraram de vez na rotina de nossas empresas.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/07/compilado-noticias-ansegtv.png" width="480" height="auto" alt="Ano da ANSEGTV" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">Constantes reuniões virtuais entre todas as filiadas foram essenciais para o bom andamento deste processo. Nestes encontros, foi fornecido todo o suporte jurídico sobre leis e Medidas Provisórias decorrentes da pandemia, além da enriquecedora troca de experiências sobre ações e desafios enfrentados.</p>

                            <p class="article">Importante ressaltar que, mesmo em meio às dificuldades, a ANSEGTV obteve importantes avanços a exemplo das reuniões com o Governo Federal, que merecem destaque entre as atividades da associação.</p>
                            <p class="article">Com o apoio da Frente Parlamentar em Defesa do Setor de Serviços, liderada pelo deputado federal Laércio Oliveira (PP-SE), o presidente da ANSEGTV, Gabriel Damasceno, ao lado de outros representantes de companhias e entidades, pôde apresentar sugestões e tratar das principais dificuldades das empresas, responsáveis por gerar um grande número de empregos.</p>
                            <p class="article">Houve videoconferências com o ministro da Economia, Paulo Guedes, com o secretário de Produtividade, Emprego e Competitividade, Carlos da Costa, com o secretário especial de Previdência e Trabalho, Bruno Bianco Leal, e com o secretário especial da Receita Federal, José Barroso Tostes Neto.</p>
                            <p class="article">Nestes encontros, foram discutidos temas como a desoneração dos tributos sobre a folha de pagamentos, a necessidade da Reforma Tributária, a prorrogação ou não de medidas relacionadas a diferimento de tributos, uma possível reformulação do Refis, entre outros assuntos, e o governo mostrou-se sempre disposto a ajudar e a estudar meios de contribuir com o setor de serviços.</p>
                            <p class="article">A associação também passou a participar do Grupo de Trabalho do ENCAT (Encontro Nacional dos Coordenadores e Administradores Tributários), que promove reuniões periódicas para discutir o aprimoramento e a criação de documentos fiscais eletrônicos do segmento de transporte de valores. Em abril, por exemplo, foi estabelecida a nova GTV-e (Guia de Transporte de Valores Eletrônica), considerada mais moderna e eficiente. A nova ferramenta possibilita a padronização de informações, uma maior integração com a SEFAZ (Secretaria de Estado da Fazenda) e a diminuição de custos, e a aproximação da ANSEGTV com a SEFAZ tem viabilizado todo o suporte necessário aos associados para o desenvolvimento do projeto.</p>
                            <p class="article">Uma live em junho, no canal do CT Segurança do YouTube, também reuniu representantes das associadas para um debate sobre os desafios da pandemia. A audiência foi altamente positiva, com quase mil visualizações até o momento.</p>
                            <p class="article">Ao analisar o trabalho realizado na primeira metade do ano, Damasceno faz questão de elogiar todos os associados. “A comunhão e reciprocidade entre os membros é essencial para o enfrentamento de tantos desafios. Sem isto, não estaríamos conseguindo vencer os obstáculos. O trabalho é árduo, mas as conquistas valem a pena”.</p>
