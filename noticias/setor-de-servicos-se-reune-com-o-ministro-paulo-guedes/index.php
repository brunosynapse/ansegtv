                            <h1>Setor de serviços se reúne com o ministro Paulo Guedes</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>20/05/2020 - 18h22</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Cerca de 30 representantes do setor de serviços participaram nesta terça-feira (19) de uma videoconferência com o ministro da Economia, Paulo Guedes. O encontro, organizado pela Frente Parlamentar em Defesa do Setor de Serviços, liderada pelo Deputado Laércio Oliveira (PP-SE), contou com a participação do presidente da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Gabriel Damasceno.</p>

                            <p class="article">Na reunião, foram discutidas propostas para a recuperação da economia pós pandemia de COVID-19 e medidas que garantam a manutenção e criação de novas vagas de trabalho.</p>
                            <p class="article">Damasceno considerou o encontro bastante positivo. “Nos motivou bastante os planos do governo para a retomada econômica, quando a crise de saúde estiver sob controle”, avaliou.</p>
                            <p class="article">Guedes falou ao grupo sobre os planos em andamento do governo que envolvem projetos de desoneração da folha de pagamentos, o parcelamento de impostos e outras ações de fomento ao emprego formal, que em breve deverão ser divulgadas.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2020/05/ministerio-economia.png" width="480" height="auto" alt="Ministério da Saúde" class=" img-fluid">	
                                <figcaption>Fonte: Agência Senado</figcaption>
                            </figure>
                            <p class="article">Segundo o ministro, uma flexibilização da legislação pode garantir nos próximos três anos até 20 milhões de empregos. Desde o início do governo, Guedes sempre defendeu a modernização das regras trabalhistas, mais simples e com menos encargos.
                            </p>
                            <p class="article">Uma das demandas do setor de serviços é a liberação de depósitos recursais feitos pelas empresas em ações trabalhistas, hoje engessados. Na reunião, empresas e associações também manifestaram preocupação com crédito e pediram mais prazo para o diferimento de tributos.</p>
                            <p class="article">O deputado federal Laércio Oliveira (PP-SE), presidente da Frente Parlamentar em Defesa do Setor de Serviços, defendeu a reedição da MP do Contrato Verde e Amarelo, que acabou perdendo sua validade. O ministro citou que o governo deverá apresentar em breve uma nova MP sobre o assunto, porém bem mais abrangente com o intuito de incentivar fortemente a criação de novos empregos formais.</p>
                            <p class="article">Em breve também deverá ser enviada ao Congresso a Reforma do PIS/Cofins, a qual o setor de serviços se colocou à disposição para ajudar na formulação do texto.</p>
                            <p class="article">Laércio concorda com a unificação dos dois tributos, mas defende faixas variadas de acordo com o setor.</p>
                            <p class="article">“Por exemplo, o setor com preponderância de mão de obra poderia compensar mais para que não houvesse aumento de impostos e que possamos proteger os empregos”, disse Laércio.</p>
                            <p class="article">A Reforma Tributária também foi tema da videoconferência. Laércio lembrou ao ministro que estava tratando do assunto com a assessora especial do Ministério da Economia, Vanessa Canado.</p>
                            <p class="article">“Apresentamos uma sugestão de emenda com alíquotas variadas”, afirmou Laércio, acrescentando que as negociações estavam avançadas, mas que a pandemia mudou toda agenda política e econômica.</p>
