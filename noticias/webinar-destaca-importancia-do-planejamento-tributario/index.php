                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">O assessor tributário da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Andrey Ferreira, e Rubens Tavares, sócio da BMS Projetos & Consultoria, debateram em webinar nesta terça-feira (28) questões previdenciárias em tempos de pandemia.</p>
                            <p class="article">O planejamento tributário foi um dos assuntos principais do evento. Andrey destacou que a medida é essencial para se atingir um fluxo de caixa mais rentável e, para isto, foram apresentadas algumas alternativas que podem ser estudadas pelas empresas para levantamento de créditos, retificações e compensações.</p>
                            <p class="article">Rubens explicou que, devido ao momento atual, cresceu o número de empresas em busca de serviços especializados em recuperação de créditos previdenciários.</p>
                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/redimensionadas/017.png" width="480" height="auto" alt="Andrey Ferreira" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">Os especialistas esclareceram ainda como as empresas podem se beneficiar de oportunidades previdenciárias no cenário de crise, como a redução de riscos, incluindo passivos de contribuições para o INSS e os decorrentes de acidentes do trabalho.</p>

                            <p class="article">Segundo Andrey, a pandemia acelerou processos e obrigou as empresas a se adaptarem. “Estamos vivendo dez anos em três, quatro meses”, disse o tributarista.</p>
                            <p class="article">Andrey ressaltou ainda que a legislação ganhou força e que as últimas medidas anunciadas pelo governo reforçam a necessidade de sua correta interpretação.</p>
                            <p class="article">O planejamento tributário ganha ainda mais relevância devido ao calendário que se tem pela frente, reforçou Rubens. O diferimento de três meses das contribuições previdenciárias, por exemplo, terminou. “Também temos a primeira parcela do décimo terceiro em novembro”, lembrou.</p>
