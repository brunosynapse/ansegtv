                            <h1>Presidente da ANSEGTV enaltece parceria com a PF e investimentos em segurança</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i>  AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>20/03/2020 - 09h15</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">O primeiro ano da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) testemunhou avanços para o segmento de transporte de valores, mas os desafios ainda são muitos. Esta é a avaliação do presidente da entidade, Gabriel Damasceno, que projeta nesta entrevista os próximos passos da organização na defesa da livre-concorrência.</p>
                            <p class="article">
                                <strong>Quais os principais avanços que o sr. destacaria neste começo de atividades da ANSEGTV?</strong>
                                <br>
                                Foram várias conquistas. Valorizo muito as reuniões que tivemos em Londres com as resseguradoras. A questão securitária é de vital importância para a operação de custódia e transporte de valores. O mercado de resseguros Lloyd´s of London é mundialmente reconhecido como líder no segmento de contenção de riscos. Na capital britânica, tivemos a oportunidade de nos apresentar, mostrar nossos propósitos e divulgar as empresas que integram a ANSEGTV. Com isso, pretendemos assegurar melhores condições para todo o segmento de transporte de valores no Brasil.
                            </p> 
                            <p class="article">
                                <strong>Atualmente, fala-se muito na necessidade da Reforma Tributária. De que forma sua aprovação afetaria o setor de transporte de valores?</strong>
                                <br>
                                Já contamos com o apoio de uma consultoria de renome no mercado que identificou os possíveis impactos para o setor de serviços. O mais provável é que ocorra uma unificação de tributos, com uma maior desoneração da indústria. Com isso, a carga tributária de setor de serviços deve ser afetada. Diante disso, foi criada uma frente parlamentar a favor desse setor pelo deputado Laércio Oliveira (PP-SE). A ANSEGTV é uma das entidades participantes do Conselho Gestor da frente e seu lançamento está previsto para abril. Até lá, mais entidades deverão aderir ao grupo.
                            </p>
                            <p class="article">
                                <strong>Como o sr. vê a questão da lavagem de dinheiro no setor de transporte de valores?</strong>
                                <br>
                                Nossa associação está totalmente alinhada com as orientações da Polícia Federal sobre o assunto. Recentemente, nós apoiamos a Coordenação Geral de Controle da Segurança Privada na produção de um manual para combater esse tipo crime. O documento traz diretrizes vitais para o enfrentamento dessa prática ilegal, especialmente a forma como devem ser transmitidas as informações ao COAF (Conselho de Controle de Atividades Financeiras).
                            </p>
                            <img src="../../img/uploads/2020/03/presidente-ansegtv.jpg" width="480" height="auto" alt="Presidente da ANSEGTV" class="pull-right m-2 img-fluid">
                            <p class="article">
                                <strong>E a questão da segurança pública em geral?</strong>
                                <br>
                                Essa é uma das nossas preocupações constantes. Visitamos periodicamente as empresas associadas à ANSEGTV para saber como estão tratando essa questão. Também tivemos reuniões importantes com secretários de Segurança Pública nos Estados para discutirmos ações em conjunto. Desde o início, dar voz às pequenas e médias empresas diante das autoridades é um dos nossos principais objetivos.
                            </p>
                            <p class="article">
                                <strong>Como o sr. avalia as últimas decisões do Cade relacionadas às aquisições no transporte de valores?</strong>
                                <br>
                                Acredito que o Cade (Conselho Administrativo de Defesa Econômica) se mostra cada vez mais preocupado com a alta concentração no setor. Recentemente, o órgão analisou e aprovou processos de aquisições neste segmento com restrições, objetivando a proteção do livre mercado. Isso já é muito positivo. A ANSEGTV defende a livre iniciativa e concorrência nesta atividade visando beneficiar todos os envolvidos, clientes e sociedade, em um mercado altamente concentrado em apenas três empresas, sendo duas multinacionais.
                            </p>
                            <p class="article">
                                <strong>O Estatuto da Segurança Privada segue em tramitação no Senado. Qual a posição da ANSEGTV sobre esse tema?</strong>
                                <br>
                                O Estatuto da Segurança Privada (SCD 006/2016) proporcionará grandes benefícios ao setor, uma maior segurança jurídica e a modernização da regulamentação da atividade de segurança privada no país, gerando inclusive milhares de empregos em todo o segmento.
                                <br>
                                A ANSEGTV apoia a aprovação do estatuto, com exceção dos parágrafos 2º ao 5º, do artigo 20, que tratam de tema exclusivamente concorrencial, que favorece a continuidade da concentração do mercado de transporte de valores, além de serem totalmente alheios aos objetivos do estatuto e aos anseios da grande maioria das empresas que atuam nos diversos segmentos do mercado de Segurança Privada.
                            </p>
